                            <h2 class="h3 font-weight-bold mt-4 mb-4">Olsztyn powojenny </h2>
                            <p> Raz stałem w kolejce po zapomogę w państwowym urzędzie repatriacyjnym. Patrzę, a tam moi dwaj koledzy z kompanii szturmowej. Wychodzę z kolejki, witam się, ale oni jakoś nie bardzo chcieli ze mną gadać, to wróciłem na swoje miejsce. Przyjechał kasjer z pieniędzmi, a ci moi koledzy odchylili płaszcze, wyciągnęli broń, rozbroili policjantów, zabrali kasę i uciekli. Ja tam stoję, alarm, myślę jak tu się wycofać, a już wojsko przyjechało. No, to stałem. Potem wróciłem do domu, spotkałem koleżankę, a ta mówi, że „jaki ja odważny”, że ludzie z kolejki mówili, że taka odwaga – wojsko wszędzie, a ten ani drgnął. Myśleli, że ja jestem wspólnikiem tamtych. I tak zostałem bohaterem – opowiada Józef Rusak.
                                <footer class="blockquote-footer">jest to relacja jednego z "komandosów" Łupaszki, moim zdaniem bardziej oddaje klimat tamtych czasów 1945-48 </footer>
                                Zastanawiam mnie, jak się czuli ludzie w kolejce zwykli przesiedleńcy, którzy nie mieli na chleb.
                                Uczciwie mówiąc to początek lat 90 w III RP był podobny, ludzie bez pracy i pieniędzy, tylko złodzieje byli nazywani złodziejami, oszuści oszustami , a ofiary ofiarami. Gdy koalicja PO-PSL, z inicjatywy PiS 1 marca 2011 r. ustanowiła dniem żołnierzy wyklętych, zatarła się różnica między ofiarami i sprawcami.
                            </p>

                            <img title="Homonto" class="img-thumbnail float-right ml-3 mt-2" src="./../img/Homonto.jpg" alt="Koniki w homontach" />

                            <p class="lead">A skoro jesteśmy przy Olsztynie to po 1945 r. pozostało wielu Niemców w rejonie Stawigudy, którzy uważali się się za rodowitych Warmiaków. Ich pierwsze kontakty z „żołnierzami wileńskiej brygady”, były bardzo drastyczne. Podczas przeszukań domostw rekwirowali co chcieli, Niemcy jako nieznający języka polskiego, po prostu nie wiedzieli czego się od nich żąda, co z kolei traktowane było jak opór i prowadziło do ciężkiego pobicia , czasami zabójstwa na miejscu . Osobiście rozmawiałem pod koniec lat 80 z matką i córką w Olsztynie, które po takiej akcji zostały ubrane w homonta i zaprzężone do wozu, jak zwierzęta pociągowe w celu odwiezienia łupów Łupaszce, który jak „ Nikoś” przebywał w Gdańsku i zarządzał terenem. Zresztą w trójkącie Pieniężno-Orneta-Lidzbark Warm. ludzie Łupaszki zwani "wileńszczukami" zaprzegali Niemki do bryczek i jeździli na nich do miasta na rynek. Czuli się bezkarnie , gdzyż uchodzili za ludzi z "lasu" , a jednocześnie wspólpracowali z MO. Zdarzały się nawet przypadki, gdy pomagali bezpośrednio w likwidacji niewygodnych byłych akowców np. w rejonie Ornety, gdzie został zamordowany brat mojego dziadka. Rozpuszczali fałszywe plotki o swoich przeciwnikach , sugerujące ich współpracę z UB , jednocześnie sami "robiąc" na nich donosy. Okradali najbliższych sasiadów, nie mając pojęcia, że czynią coś złego. Taką bronią walczyli na ogół ludzie Łupaszki. Byli bezwzględni i skuteczni, ale czy z tego mogło cokolwiek dobrego wyniknąć dla Ziem Odzyskanych ? Ci ludzie zostali kompletnie oderwani od rzeczywistości, nie mieli bladego pojęcia co dzieje się na świecie i jak zakończyła się II Wojna Światowa.
                            </p>

                            <h2 class="h3 font-weight-bold mt-4 mb-4">Jeżeli nie wiadomo o co chodzi tzn. że chodzi o pieniądze ! </h2>
                            <blockquote class="blockquote">
                                <p> Musimy na wstępie wyjaśnić sobie o jakich pieniądzach rozmawiamy, a jest to w istocie bardzo ciekawe. Na dzień przed 1 września 1939 r. w II RP pomoc domowa zarabiała 35 zł, murarz w W-wie 135 zł, a taki Łupaszka jako dowódca brygady zarabiałby miesięcznie 1200 zł. Ideologia była w takiej sytuacji na planie drugim. Żołnierz zawodowy, który tyle lat żył dostatnio na koszt podatnika miał do wyboru kilka opcji honorowych : po pierwsze mógł zginąć na polu chwały, po drugie mógł dostać się do niewoli, po trzecie przedostać się przez granicę i kontynuować walkę. Jeżeli ktoś uciekł z pola walki i po kilku latach wojny zaczął wykonywać wyroki śmierci na rzekomych kolaborantach był po prostu tchórzem. Dla mnie więcej odwagi miał taki sekretarz partii który samotnie szedł organizować oświatę na wsi niż te " tłumoki wyklęte" co pisać i czytać nie potrafili, ale przy pomocy kłonic i siekier, całą bandą pod osłoną nocy go napadli. Przeniesienie ideologi żołnierzy wyklętych jest bardzo wygodne w dzisiejszych czasach zwłaszcza politykom taka opcja robienia niczego bez odpowiedzialności i konsekwencji , bardzo przypadła do gustu. A pozyskiwanie majątków taką metodą na "Łupaszkę" stało się domeną zarówno osób świeckich jak i kościelnych. Metoda jest prosta. Opieka w zamian za przepisanie majątku. Dopóki ofiara może pracować i sama o siebie zadbać jest okey problem zaczyna się w przypadku choroby. Nikt nie zgłasza się po odbiór takich osób i trafiają do hospicjum, co w polskim wydaniu jest wyrokiem śmierci. A pamiętajmy, że ludzie Łupaszki byli partyzantką katolicką , która dzień zaczynała i kończyła różańcem. Z opisów wiemy do czego byli zdolni. Gdy czerpie się z takich wzorców, to nic dziwnego, że moralnie jesteśmy tu gdzie jesteśmy. Gdyby dziś mnie ktoś zapytał. Z czym bym poszedł w 1945 r. na warmińską wieś jako członek bandy z bronią w ręku, czy samotnie z elementarzem pod pachą? Wybrałbym elementarz choć bardzo często było to równoznaczne ze śmiercią.
                                    Jeżeli z takich ludzi jak Zygmunt Szendzielarz, robimy bohaterów, to nie ma sensu zaprzeczać, że mordowaliśmy Żydów po wojnie.
                                </p>
