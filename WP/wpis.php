						<p>Znany niemiecki krytyk Marcel Reich-Ranicki wywołał w Polsce wzburzenie, kiedy w słynnym, nieistniejącym już telewizyjnym "Kwartecie literackim" zjechał współczesną polską literaturę. Orzekł wówczas, że książki Olgi Tokarczuk, Andrzeja Stasiuka i Małgorzaty Tulli to ciemnogród i powrót do średniowiecza. Wywoływanie emocji to zresztą jego specjalność - w dużej mierze dzięki swoim kategorycznym, wypowiadanym bez ogródek sądom stał się tym, kim jest - wyrocznią i "papieżem" niemieckiej krytyki literackiej. </p>

						<p class="lead">Polska jest traktowana jak Afryka , gdyż poprawność polityczna nakazuje pisać pozytywne komentarze </p>

						<h2 class="h3 font-weight-bold mb-4">A co mamy robić ?</h2>

						<p>Tłumaczą się niemieccy krytycy sztuki. Już dość krzywdy wyrządziliśmy Polakom w czasie wojny ! To właśnie kompleksami jakie mają Niemcy w stosunku do Słowian , nieżyjący już nasz rodak Reich-Ranicki tłumaczył, brak uczciwej krytyki i bardzo pochlebne opinie, na temat polskiej sztuki </p>

						<p>W naszym kraju twórczość Olgi Tokarczuk budzi wiele kontrowersji. Nagroda Nobla i Księgi Jakubowe dolały oliwy do ognia. Polacy swoje żale przelewają na Żydów i Niemców, oskarżając ich o zlecenie napisania tych dzieł. Tu poprawność polityczna nakazuje użyć słowa starozakonni, albo starsi bracia w wierze .</p>
						<p><strong>Jeśli martwi Was ten groch z kapustą na wstępie, to tak wygląda 1000 stron dzieła Księgi Jakubowe. Jest to nowoczesny sposób Olgi Tokarczuk do kontaktowania się z czytelnikiem</strong> .</p>

						<p class="lead">Ja osobiście, gdy zasiadłem do lektury Ksiąg Jakubowych, bardzo szybko zdałem sobie sprawę , że już ten język i retorykę znam. Pod koniec lat osiemdziesiątych brałem udział w wielu dyskusjach, na temat Akcji Wisła i 400-letniej polskiej okupacji Ukrainy. To właśnie wtedy po raz pierwszy zetknąłem się z ludźmi, którzy mówią o nas tak jak my mówimy o Niemcach z II wojny . Można wierzyć lub nie, ale są jeszcze wykształceni Ukraińcy, absolwenci liceum ukraińskiego np. z Bartoszyc lub Górowa Iławeckiego, którzy w szkole na terenie naszego kraju dowiadywali sie o polskiej okupacji Krakowa.</p>

