						<h2 class="h3 font-weight-bold mb-4">Daj, ać ja pobruszę, a ty poczywaj</h2>

						<p>Po przetrawieniu powieści czytelnik ma wrażenie ,że śniło mu się <strong>Pismo Święte</strong>, czasami Pan Tadeusz lub Ogniem i Mieczem, a przede wszystkim Chłopi Reymonta - takie są przynajmniej pozytywne opinie . Ja miałem wrażenie, że autorka przeniosła mnie w XV wiek, gdzieś na Łysą Górę i pierwszy raz czytam <strong>Lamenty świętokrzyskie</strong> których fragmenty zamieściłem w panelu bocznym i w pełni zgadzam się z Reichem-Ranickim ,że twórczość Olgi Tokarczuk to powrót do średniowiecza . Ostatnio odnoszę wrażenie, że powstała w naszym kraju świecka tradycja podziału Polaków na dobrych Polaków i tych mniej dobrych. Jeżeli piszesz o polskich wadach to jesteś tolerancyjnym europejczykiem, jeśli powiesz coś w duchu patriotycznym ,to jesteś nacjonalistą, faszystą, a jeśli do tego jesteś biskupem-patriotą stajesz się pedofilem. Pani Olga szykuje nam na jesień niespodziankę podobno zaskoczy czytelnika i będzie o stosunku Polaków do Ukraińców, co w dobie walki młodych ludzi o miejsca pracy , jest jak najbardziej słuszną koncepcją . Mogę sobie wyobrazić reakcję i komentarze.
						</p>
