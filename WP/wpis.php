								Zaczynam myśleć, że my śniliśmy własną historię. Wymyśliliśmy sobie historię Polski, jako kraju niezwykle tolerancyjnego, otwartego, jako kraju, który nie splamił się niczym złym w stosunku do swoich mniejszości powiedziała. Tymczasem, jak podkreśliła, w rzeczywistości zamietliśmy pod dywan pogromy w czasie wojny i okresie powojennym. Zaczęliśmy sobie wyobrażać, że jesteśmy narodem, który wyciąga ręce, walczy o wolność waszą i naszą. – Ale kiedy przyszedł test, okazuje się, że nie jesteśmy w stanie zdać tego egzaminu
